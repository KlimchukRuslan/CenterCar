package com.centercity.server.tempobj;

public class ShiftCar {
    String car_number;
    String old_driver;
    String new_driver;
    String time;
    String probeg;

    public String getProbeg() {
        return probeg;
    }

    public void setProbeg(String probeg) {
        this.probeg = probeg;
    }

    public String getCar_number() {
        return car_number;
    }

    public void setCar_number(String car_number) {
        this.car_number = car_number;
    }

    public String getOld_driver() {
        return old_driver;
    }

    public void setOld_driver(String old_driver) {
        this.old_driver = old_driver;
    }

    public String getNew_driver() {
        return new_driver;
    }

    public void setNew_driver(String new_driver) {
        this.new_driver = new_driver;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
